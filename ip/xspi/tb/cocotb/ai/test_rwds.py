# ── Register addresses (sccr.rdl regwidth=64, alignment=64) ──────────────────
REG_CFG              = 0x10   # InitialLatency[7:4]
REG_XSPI_STATUS      = 0x18   # wip[0]
REG_XSPI_CONTROL     = 0x20   # interrupt_enable[1]
REG_XSPI_RATES       = 0x28
REG_INTERRUPT_STATUS = 0x30   # axi_resp[1:0], read_underflow, write_overflow

# ── Constants ─────────────────────────────────────────────────────────────────
MEM_BYTES  = 8          # one xSPI memory word = 64 bits
WRITE      = False      # rwds_in = 0  →  byte IS written
MASK_BYTE  = True       # rwds_in = 1  →  byte is NOT written

# Modes that support write-with-mask (DDR/SDR modes with RWDS)
WRITE_MODES = [
    (Mode.S1, Mode.S1, Mode.S1),
    (Mode.S4, Mode.D4, Mode.D4),
    (Mode.D4, Mode.D4, Mode.D4),
    (Mode.S8, Mode.S8, Mode.S8),
    (Mode.D8, Mode.D8, Mode.D8),
]

CORNER_DATA = [
    0xAABBCCDDEEFF0011,
    0x0000000000000000,
    0xFFFFFFFFFFFFFFFF,
    0xAA55AA55AA55AA55,
    0x5555555555555555,
    0x0102030405060708,
    0xFEDCBA9876543210,
    0xF0F0F0F0F0F0F0F0,
]

random.seed(0xBEEF0001)
ADDRS = [random.randint(0, 2**28) & 0xfffffff8 for _ in range(60)]


# ── Helpers ───────────────────────────────────────────────────────────────────

async def _boot(dut, default_mode_pin: int) -> Env:
    """Standard boot sequence matching test_default.py pattern."""
    dut.cfg_default_mode_m.value = default_mode_pin
    env = Env(dut)
    env.cmd.set_Default_Mode(default_mode_pin)
    await env.boot()
    return env


def _expected_bytes(orig: int, new: int, mask: list) -> bytes:
    """Return expected 8-byte memory content after a masked write.

    mask[i] = WRITE (False) → byte i = new_val byte i
    mask[i] = MASK  (True)  → byte i = orig_val byte i  (unchanged)
    Byte 0 = least-significant byte.
    """
    ob = list(orig.to_bytes(MEM_BYTES, "little"))
    nb = list(new.to_bytes(MEM_BYTES, "little"))
    return bytes(nb[i] if not mask[i] else ob[i] for i in range(MEM_BYTES))


def _clocks_per_byte_write(mode: Mode) -> int:
    """Total clock edges generated by _write_byte() for one byte in given mode.

    Derived from master_driver._write_byte() implementation:
      S1: 8 × (RisingEdge + FallingEdge) = 16
      D1: 4 × (RisingEdge + FallingEdge) =  8
      S4: 2 × (RisingEdge + FallingEdge) =  4
      D4: 1 × (RisingEdge + FallingEdge) =  2
      S8: 1 × (RisingEdge + FallingEdge) =  2
      D8: 1 × Edge(any)                  =  1
    """
    return {
        Mode.S1: 16,
        Mode.D1:  8,
        Mode.S4:  4,
        Mode.D4:  2,
        Mode.S8:  2,
        Mode.D8:  1,
    }[mode]


def _write_mem_preamble_edges(cmd_mode: Mode, addr_mode: Mode,
                               has_ext: bool, n_addr: int) -> int:
    """Number of clock edges before the first data byte for write_Mem.

    WriteMEM format (D1 for non-S1, K0 for S1):
      D1: extension=True,  address=4 bytes
      K0: extension=False, address=4 bytes
    """
    cpb_cmd  = _clocks_per_byte_write(cmd_mode)
    cpb_addr = _clocks_per_byte_write(addr_mode)
    ext_edges = cpb_cmd if has_ext else 0
    return cpb_cmd + ext_edges + n_addr * cpb_addr


# ── RwdsInSampler ─────────────────────────────────────────────────────────────

class RwdsInSampler:
    """Captures xspi_rwds_in at every clock edge while CS# is asserted.

    Usage:
        sam = RwdsInSampler(dut)
        sam.start()
        await env.cmd.write_Mem(addr, data, mask=mask)
        sam.stop()
        data_vals = sam.data_phase_values(cmd_mode, addr_mode, data_mode, has_ext=True)
        assert data_vals == [int(m) for m in mask]
    """

    def __init__(self, dut):
        self.dut = dut
        self._raw: list = []    # [(time_ns, rwds_in_val)] sampled while csn=0
        self._task = None

    def start(self):
        self._raw = []
        self._task = cocotb.start_soon(self._run())

    def stop(self):
        if self._task is not None:
            self._task.cancel()
            self._task = None

    async def _run(self):
        while True:
            await Edge(self.dut.xspi_clk)
            if int(self.dut.xspi_csn.value) == 0:
                t = cocotb.utils.get_sim_time("ns")
                v = int(self.dut.xspi_rwds_in.value)
                self._raw.append((t, v))

    def all_values(self) -> list:
        """All captured (time_ns, rwds_in) pairs while CS# was low."""
        return list(self._raw)

    def data_phase_values(self, cmd_mode: Mode, addr_mode: Mode,
                          data_mode: Mode, has_ext: bool,
                          n_addr: int, n_data_bytes: int) -> list:
        """Extract one rwds_in value per data byte from the captured sequence.

        Uses the first edge of each data byte (all edges within a byte carry
        the same rwds_in value since _write_byte sets it once per byte call).

        Returns a list of int (0 or 1), one per data byte.
        """
        preamble = _write_mem_preamble_edges(
            cmd_mode, addr_mode, has_ext, n_addr
        )
        cpb_data = _clocks_per_byte_write(data_mode)
        raw_vals = [v for _, v in self._raw]

        result = []
        for byte_idx in range(n_data_bytes):
            edge_idx = preamble + byte_idx * cpb_data
            if edge_idx < len(raw_vals):
                result.append(raw_vals[edge_idx])
            else:
                cocotb.log.warning(
                    f"RwdsInSampler: insufficient samples for byte {byte_idx} "
                    f"(edge_idx={edge_idx}, total={len(raw_vals)})"
                )
                result.append(None)
        return result

    def assert_all_zero_preamble(self, cmd_mode: Mode, addr_mode: Mode,
                                  has_ext: bool, n_addr: int, tag: str = ""):
        """Assert that every preamble (CMD/EXT/ADDR) edge has rwds_in=0."""
        preamble = _write_mem_preamble_edges(
            cmd_mode, addr_mode, has_ext, n_addr
        )
        raw_vals = [v for _, v in self._raw]
        for idx in range(min(preamble, len(raw_vals))):
            assert raw_vals[idx] == 0, (
                f"{tag} rwds_in={raw_vals[idx]} at preamble edge {idx} "
                f"(expected 0: CMD/EXT/ADDR must not assert byte-enable)"
            )

    def log_sequence(self, tag: str = ""):
        vals = [v for _, v in self._raw]
        cocotb.log.info(
            f"  {tag} rwds_in sequence ({len(vals)} edges): "
            + "".join(str(v) for v in vals)
        )


