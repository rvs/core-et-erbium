async def test_oe_masked_write_transaction(dut, mode, latency, default_mode_pin):
    """OE-WM: OE behaviour must be identical for masked writes.

    Even with all bytes masked (rwds_in=1), the DUT must keep
    dq_out_ena=0 and rwds_out_ena=0 during the write DATA phase.
    """
    cocotb.log.info(
        f"OE-WM masked write OE {mode[0].name} latency={latency} pin={default_mode_pin}"
    )
    env = await _boot(dut, default_mode_pin)
    await env.cmd.setLatency(latency)
    assert int(dut.dut.latency_count.value) == latency
    await env.cmd.setRate(*mode)

    env.cmd.check_enables = True

    masks = [
        [MASK_BYTE] * MEM_BYTES,                                        # all masked
        [WRITE, MASK_BYTE] * (MEM_BYTES // 2),                         # alternating
        [WRITE]*4 + [MASK_BYTE]*4,                                      # lower half
    ]
    for i, mask in enumerate(masks):
        addr = ADDRS[3 + i]
        val  = CORNER_DATA[i]
        cocotb.log.info(
            f"  masked-write {i}: mask={[int(m) for m in mask]}"
        )
        await env.cmd.write_Mem(addr, val.to_bytes(MEM_BYTES, "little"), mask=mask)

    env.cmd.check_enables = False
    await env.assert_no_xspi_errors(msg=f"OE-WM mode={mode[0].name}")


