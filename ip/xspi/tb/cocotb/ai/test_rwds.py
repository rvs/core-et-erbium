@cocotb.test(timeout_time=40000, timeout_unit="ns")
@cocotb.parametrize(
    mode=WRITE_MODES,
    default_mode_pin=[1, 2, 3],
)
async def test_rwds_in_all_zero_unmasked_write(dut, mode, default_mode_pin):
    """RWDS-W2: write_Mem(mask=None) → rwds_in=0 at every data-phase clock edge.

    BUG-9 in the old driver: masked=None was assigned directly to a LogicArray,
    producing undefined behaviour. The fix is ``msk = False`` when mask is None
    (line 133 of the updated master_driver.py). This test confirms the fix.
    """
    cocotb.log.info(
        f"RWDS-W2 rwds_in=0 unmasked {mode[0].name} pin={default_mode_pin}"
    )
    env = await _boot(dut, default_mode_pin)
    await env.cmd.setLatency(17)
    assert int(dut.dut.latency_count.value) == 17
    await env.cmd.setRate(*mode)
    env.cmd.check_enables = False   # functional focus, OE checked separately

    # Determine format for WriteMEM
    if env.cmd.cmd_mode in [Mode.S1, Mode.D1]:
        fmt = command_table[Cmd.WriteMEM]["fmt_1s"]   # K0: no extension, 4B addr
    else:
        fmt = command_table[Cmd.WriteMEM]["fmt_8s"]   # D1: extension, 4B addr
    has_ext = format_table[fmt]["extension"]
    n_addr  = format_table[fmt]["address"]

    sam = RwdsInSampler(dut)
    sam.start()

    addr = ADDRS[10]
    val  = 0x0102030405060708
    await env.cmd.write_Mem(addr, val.to_bytes(MEM_BYTES, "little"))
    # mask=None → every byte written → rwds_in must be 0

    for _ in range(4):
        await RisingEdge(dut.xspi_clk)
    sam.stop()
    sam.log_sequence(tag=f"RWDS-W2 {mode[0].name}")

    data_vals = sam.data_phase_values(
        mode[0], mode[1], mode[2], has_ext, n_addr, MEM_BYTES
    )
    cocotb.log.info(f"  data-phase rwds_in per byte: {data_vals}")

    for i, v in enumerate(data_vals):
        assert v == 0, (
            f"RWDS-W2 BUG: byte[{i}] rwds_in={v} (expected 0). "
            f"masked=None must map to rwds_in=0, not undefined. "
            f"mode={mode[0].name}"
        )

    rv = await env.cmd.read_Mem(addr)
    assert hex(int.from_bytes(rv, "little")) == hex(val), (
        f"RWDS-W2 write-back mismatch: expected=0x{val:016x} "
        f"got=0x{int.from_bytes(rv,'little'):016x}"
    )
    await env.assert_no_xspi_errors(msg="RWDS-W2")


