    cocotb.log.info(f"  tx_full={bool(status & (1 << STS_TX_FULL))}, tx_empty={bool(status & (1 << STS_TX_EMPTY))}")
    await wait_tx_empty(tb, timeout_us=5000)
    cocotb.log.info("  TX FIFO drained")

    # --- RX FIFO fill via UartSource ---
    cocotb.log.info("--- Filling RX FIFO with 16 frames via UartSource ---")
    payload = bytes([(0x10 + i) & 0xFF for i in range(RX_FIFO_DEPTH)])
    await tb.uart_tx.write(payload)
    await tb.uart_tx.wait()
    await Timer(max(10, frame_ns * (RX_FIFO_DEPTH + 2) // 1000), 'us')

