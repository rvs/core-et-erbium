def safe_int(logic_array):
    """Convert a cocotb LogicArray to int, masking X/Z to 0."""
    try:
        return logic_array.integer
    except ValueError:
        bin_str = logic_array.binstr.lower().replace('x', '0').replace('z', '0')
        return int(bin_str, 2)

