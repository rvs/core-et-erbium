@cocotb.test(timeout_time=5, timeout_unit="ms")
async def test_12_stress_random(dut):
    """
    Randomised protocol configuration stress test.
    Tests multiple charsize × parity × stopbits combinations.
    """
    tb = await _tb_init(dut)

    cocotb.log.info("=" * 60)
    cocotb.log.info("TEST 12: Stress / Random Configuration Test")
    cocotb.log.info("=" * 60)

    clk_freq_mhz = await measure_clk_freq_mhz(dut)
    cocotb.log.info(f"Measured system_clk = {clk_freq_mhz:.3f} MHz")

    num_iterations = 2
    mismatches = 0

    for iteration in range(num_iterations):
        charsize  = random.choice([8])
        parity    = random.choice([0, 1, 2])
        stopbits  = random.choice([0, 1, 2])
        baud_val  = random.choice([2, 5, 10])
        actual_baud = int(clk_freq_mhz * 1e6 / (16 * baud_val))

        cocotb.log.info(f"--- Iter {iteration+1}/{num_iterations}: "
                        f"cs={charsize} p={parity} sb={stopbits} baud_val={baud_val} ({actual_baud} bps) ---")

        frame_ns   = calc_frame_time_ns(baud_val, charsize, parity=parity,
                                        stopbits=stopbits, clk_freq_mhz=clk_freq_mhz)
        timeout_us = max(1000, frame_ns * 5 // 1000 + 200)
        await uart_reconfigure(tb, dut, actual_baud, baud_val,
                               charsize=charsize, parity=parity, stopbits=stopbits)

        data_mask = (1 << charsize) - 1
        tx_data   = random.randint(0, data_mask)
        rx_data   = random.randint(0, data_mask)

        # TX: DUT → UartSink
        tb.uart_rx.clear()
        await tb.reg.uart_registers.TxReg.write(tx_data)
        await tb.assert_no_xspi_errors(msg="TxReg Write")
        await tb.uart_rx.wait(timeout=timeout_us, timeout_unit="us")
        if not tb.uart_rx.empty():
            tx_result = tb.uart_rx.read_nowait(1)[0]
            if tx_result != (tx_data & data_mask):
                mismatches += 1
                cocotb.log.error(f"  TX mismatch: exp 0x{tx_data & data_mask:x} got 0x{tx_result:x}")
            else:
                cocotb.log.info(f"  TX 0x{tx_result:x} ✓")
        else:
            cocotb.log.warning(f"  TX timeout iter {iteration+1}")
        await wait_tx_empty(tb, timeout_us=timeout_us)

        # RX: UartSource → DUT FIFO
        await tb.uart_tx.write(bytes([rx_data & 0xFF]))
        await tb.uart_tx.wait()
        await Timer(max(5, frame_ns * 3 // 1000), 'us')
        if await wait_rx_not_empty(tb, timeout_us=timeout_us):
            read_val = await tb.reg.uart_registers.RxReg.read()
            await tb.assert_no_xspi_errors(msg="RxReg Read")
            read_val &= data_mask
            if read_val != (rx_data & data_mask):
                mismatches += 1
                cocotb.log.error(f"  RX mismatch: exp 0x{rx_data & data_mask:x} got 0x{read_val:x}")
            else:
                cocotb.log.info(f"  RX 0x{read_val:x} ✓")
        else:
            cocotb.log.warning(f"  No RX data iter {iteration+1}")

    assert mismatches == 0, f"Stress test: {mismatches} total mismatches"

    cocotb.log.info("TEST 12: STRESS TEST PASSED")
    print("\n" + "=" * 60)
    print("TEST 12: STRESS TEST PASSED")
    print("=" * 60)
