        await tb.assert_no_xspi_errors(msg="ControlReg Write")
        await Timer(2, 'us')
        rb = await tb.reg.uart_registers.ControlReg.read()
        await tb.assert_no_xspi_errors(msg="ControlReg Read")
        rb_cs = (rb >> 5) & 0x3F
