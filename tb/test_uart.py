@cocotb.test(timeout_time=30, timeout_unit="ms")
async def test_04b_standard_baud_rates(dut):
    """
    Verify UART at all standard baud rates: 4800, 9600, 19200, 38400, 57600,
    115200.

    After PRCM programs a (potentially random) clock frequency, this test:
      1. Measures the actual system_clk frequency via simulation timing.
      2. Computes the BaudReg value that best approximates each standard rate.
      3. Skips rates with >5% error or out-of-range baud_val.
      4. Runs a TX + RX loopback at each viable rate and asserts correctness.
    """
    tb = await _tb_init(dut)

    cocotb.log.info("=" * 60)
    cocotb.log.info("TEST 04b: Standard UART Baud Rates with PRCM Clock")
    cocotb.log.info("=" * 60)

    # --- Step 1: Measure the actual UART clock frequency ---
    clk_freq_mhz = await measure_clk_freq_mhz(dut, num_cycles=16)
    cocotb.log.info(f"PRCM-generated system_clk = {clk_freq_mhz:.4f} MHz")

    # --- Step 2: Evaluate each standard baud rate ---
    STANDARD_BAUD_RATES = [4800, 9600, 19200, 38400, 57600, 115200]
    MAX_BAUD_ERROR_PCT  = 5.0   # maximum acceptable frequency error
    CHARSIZE = 8

    skipped = []
    tested  = []
    failed  = []

    for target_baud in STANDARD_BAUD_RATES:
        cocotb.log.info("-" * 55)
        cocotb.log.info(f"Target baud rate: {target_baud}")

        baud_val, actual_baud, err_pct = calc_baud_val_for_target(
            target_baud, clk_freq_mhz)

        if baud_val is None:
            cocotb.log.warning(
                f"  SKIP {target_baud}: baud_val out of [1, 65535] range "
                f"(clk={clk_freq_mhz:.3f} MHz)")
            skipped.append(target_baud)
            continue

        cocotb.log.info(
            f"  baud_val={baud_val}  actual_baud={actual_baud:.1f}  "
            f"error={err_pct:.2f}%")

        if err_pct > MAX_BAUD_ERROR_PCT:
            cocotb.log.warning(
                f"  SKIP {target_baud}: error {err_pct:.2f}% > {MAX_BAUD_ERROR_PCT}% "
                f"(clk={clk_freq_mhz:.3f} MHz, baud_val={baud_val})")
            skipped.append(target_baud)
            continue

        # --- Step 3: Reconfigure UartSource/UartSink + DUT registers ---
        frame_ns   = calc_frame_time_ns(baud_val, CHARSIZE, clk_freq_mhz=clk_freq_mhz)
        timeout_us = max(500, frame_ns * 5 // 1000 + 200)
        cocotb.log.info(f"  frame_ns={frame_ns}  timeout={timeout_us}us")

        await uart_reconfigure(tb, dut, int(actual_baud), baud_val,
                               charsize=CHARSIZE, parity=0, stopbits=0)

        test_ok = True

        # --- TX path: DUT → UartSink ---
        tx_data = 0xA5
        cocotb.log.info(f"  TX 0x{tx_data:02X} at {target_baud} baud ...")
        tb.uart_rx.clear()
        await tb.reg.uart_registers.TxReg.write(tx_data)
        await tb.assert_no_xspi_errors(msg=f"TxReg Write ({target_baud})")
        await tb.uart_rx.wait(timeout=timeout_us, timeout_unit="us")
        if not tb.uart_rx.empty():
            tx_result = tb.uart_rx.read_nowait(1)[0]
            if tx_result != tx_data:
                cocotb.log.error(f"  TX MISMATCH @ {target_baud}: expected 0x{tx_data:02X}, got 0x{tx_result:02X}")
                test_ok = False
            else:
                cocotb.log.info(f"  TX OK: 0x{tx_result:02X} ✓")
        else:
            cocotb.log.error(f"  TX FAIL @ {target_baud}: UartSink timeout")
            test_ok = False

        await wait_tx_empty(tb, timeout_us=timeout_us)

        # --- RX path: UartSource → DUT FIFO ---
        rx_data = 0x5A
        cocotb.log.info(f"  RX 0x{rx_data:02X} at {target_baud} baud ...")
        await tb.uart_tx.write(bytes([rx_data]))
        await tb.uart_tx.wait()
        settle_us = max(20, frame_ns * 3 // 1000 + 20)
        await Timer(settle_us, 'us')

        if await wait_rx_not_empty(tb, timeout_us=timeout_us):
            read_val = await tb.reg.uart_registers.RxReg.read()
            await tb.assert_no_xspi_errors(msg=f"RxReg Read ({target_baud})")
            read_val &= 0xFF
            if read_val != rx_data:
                cocotb.log.error(f"  RX MISMATCH @ {target_baud}: expected 0x{rx_data:02X}, got 0x{read_val:02X}")
                test_ok = False
            else:
                cocotb.log.info(f"  RX OK: 0x{read_val:02X} ✓")
        else:
            cocotb.log.error(f"  RX FAIL @ {target_baud}: FIFO never became non-empty")
            test_ok = False

        tested.append(target_baud)
        if not test_ok:
            failed.append(target_baud)

    # --- Summary ---
    cocotb.log.info("=" * 55)
    cocotb.log.info(f"Standard baud rate summary (clk={clk_freq_mhz:.3f} MHz):")
    cocotb.log.info(f"  Tested : {tested}")
    cocotb.log.info(f"  Skipped: {skipped}")
    cocotb.log.info(f"  Failed : {failed}")
    cocotb.log.info("=" * 55)

    assert len(failed) == 0, \
        f"Standard baud rate test FAILED for: {failed}"

    if not tested:
        cocotb.log.warning(
            "No standard baud rates were testable at the current clock frequency. "
            f"All {len(skipped)} were skipped.")
    else:
        cocotb.log.info("TEST 04b: STANDARD BAUD RATE TEST PASSED")

    print("\n" + "=" * 60)
    print(f"TEST 04b: STANDARD BAUD RATES PASSED (tested={tested}, skipped={skipped})")
    print("=" * 60)


